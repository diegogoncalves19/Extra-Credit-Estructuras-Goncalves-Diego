    private void CargarJuegoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CargarJuegoActionPerformed
        try {
            BufferedReader reader = new BufferedReader(new FileReader("src//archivotxt//datos.txt"));
            String datos = "";
            String line;
            while((line = reader.readLine()) != null){
                datos += line + "\n";
            }
            
            String[] datoslista = datos.split("\n");
            MenuPrincipal menu = new MenuPrincipal();
            menu.setWatts(Integer.parseInt(datoslista[0]));
            menu.setRegalos(Integer.parseInt(datoslista[1]));
            menu.setAmistad(Integer.parseInt(datoslista[2]));
            menu.setPokemonescogido(Integer.parseInt(datoslista[3]));
            totaltime = Long.parseLong(datoslista[4]);
            menu.setTotaltime(totaltime);
            menu.setStarttime(starttime);
            menu.setRegalosstring(datoslista[5]);
            JOptionPane.showMessageDialog(null, "Datos cargados con Ã©xito.");
            menu.setVisible(true);
            this.setVisible(false);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(CargarJuego.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(CargarJuego.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_CargarJuegoActionPerformed

